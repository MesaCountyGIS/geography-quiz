    tips = $(".validateTips");

    function checkLength(text, chars, message) {
        allFields.removeClass("error turnTipsRed");
        if (!(text.val().length > chars)) {
            tips.addClass("error turnTipsRed");
            tips.text(message);
            return false;
        } else {
            return true;
        }
    }

    function checkRegexp(o, regexp, n) {
        allFields.removeClass("error turnTipsRed");
        if (!(regexp.test(o.val()))) {
            tips.addClass("error turnTipsRed");
            tips.text(n);
            return false;
        } else {
            return true;
        }
    }

